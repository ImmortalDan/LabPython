firstYearWins = int(input('Enter number of wins in the first year.'))
secondYearWins = int(input('Enter number of wins in the second year.'))
thirdYearWins = int(input('Enter number of wins in the third year.'))
fourthYearWins = int(input('Enter number of wins in the fourth year.'))
fifthYearWins = int(input('Enter number of wins in the fifth year.'))

averageWins = (firstYearWins + secondYearWins + thirdYearWins +fourthYearWins + fifthYearWins) / 5

print("The average number of wins in the last five years is", averageWins)
